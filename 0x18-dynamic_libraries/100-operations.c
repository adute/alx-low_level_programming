#include <stdio.h>
/**
 * add - function that adds two numbers
 * @a: first num
 * @b: second num
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - function that subtract
 * @a: first num
 * @b: second num
 * Return: difference
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - function that multiply two numbers
 * @a: first num
 * @b: second num
 * Return: product
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - function that divides two numbers
 * @a: first num
 * @b: second num
 * Return: the quotient
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - function that modulo two numbers
 * @a: first num
 * @b: second num
 * Return: eqv.class
 */
int mod(int a, int b)
{
	return (a % b);
}




